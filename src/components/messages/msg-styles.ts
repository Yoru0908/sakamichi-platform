// ── R2 avatar URL (msg-avatars = member thumbnails from /v2/members) ──
export const R2_AVATAR_BASE = 'https://media.46log.com/msg-avatars';
const AVATAR_VERSION = 'v2'; // bump to bust browser cache after re-upload

// 半角カタカナ→全角カタカナ (must match upload script normalization)
const HW = 'ｦｧｨｩｪｫｬｭｮｯｰｱｲｳｴｵｶｷｸｹｺｻｼｽｾｿﾀﾁﾂﾃﾄﾅﾆﾇﾈﾉﾊﾋﾌﾍﾎﾏﾐﾑﾒﾓﾔﾕﾖﾗﾘﾙﾚﾛﾜﾝﾞﾟ';
const FW = 'ヲァィゥェォャュョッーアイウエオカキクケコサシスセソタチツテトナニヌネノハヒフヘホマミムメモヤユヨラリルレロワン゛゜';
function normalizeKana(s: string): string {
  let r = '';
  for (const c of s) { const i = HW.indexOf(c); r += i >= 0 ? FW[i] : c; }
  return r.replace(/([カキクケコサシスセソタチツテトハヒフヘホ])゛/g, (_, c) => {
    const d = 'ガギグゲゴザジズゼゾダヂヅデドバビブベボ';
    const b = 'カキクケコサシスセソタチツテトハヒフヘホ';
    return d[b.indexOf(c)] || c + '゛';
  }).replace(/([ハヒフヘホ])゜/g, (_, c) => {
    const h = 'パピプペポ'; const b = 'ハヒフヘホ';
    return h[b.indexOf(c)] || c + '゜';
  });
}

export function getR2AvatarUrl(name: string): string {
  return `${R2_AVATAR_BASE}/${normalizeKana(name).replace(/\s+/g, '_')}.jpg?${AVATAR_VERSION}`;
}

