// ── Shared member data loader ──
// Fetches from R2 (msg-avatars/members.json) first, falls back to local /data/member-images.json
const R2_MEMBERS_URL = `${R2_AVATAR_BASE}/members.json`;

export async function fetchMemberData(): Promise<Record<string, any>> {
  // Try R2 first (generated by fetch-msg-avatars-standalone.js)
  try {
    const res = await fetch(R2_MEMBERS_URL);
    if (res.ok) {
      const data = await res.json();
      if (data.images && Object.keys(data.images).length > 0) {
        return data.images;
      }
    }
  } catch { /* fall through */ }

  // Fallback to local static file
  const res = await fetch('/data/member-images.json');
  const data = await res.json();
  return data.images || {};
}

// Deduplicate: keep spaced name when both spaced and unspaced versions exist
export function deduplicateMembers(images: Record<string, any>): Record<string, any> {
  const allNames = new Set(Object.keys(images));
  const result: Record<string, any> = {};
  for (const [name, info] of Object.entries(images)) {
    if (!name.includes(' ')) {
      let hasSpaced = false;
      for (const other of allNames) {
        if (other !== name && other.includes(' ') && other.replace(/\s+/g, '') === name) {
          hasSpaced = true;
          break;
        }
      }
      if (hasSpaced) continue;
    }
    result[name] = info;
  }
  return result;
}
